class add_mult(object):
# =============================================================================
# constructor
# add x by 1
# multiply y by 1
# =============================================================================
    def __init__(self, addx=1, muly=1):
        self.addx = addx
        self.muly = muly
# =============================================================================
#
#
#
# =============================================================================
# call also overrides []
# =============================================================================
    def __call__(self, sample):
        x = sample[0]
        y = sample[1]
        x = x + self.addx
        y = y * self.muly
        return x, y
